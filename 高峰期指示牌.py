# 高峰期间 指示牌.py
'''
  模拟电信业务高峰其人流量指示牌
'''

times = ["08:00", "09:00", "10:00", "11:00", "12:00", "13:00", "14:00", "15:00", "16:00"]
peoples = [1, 3, 13, 15, 20, 12, 5, 1]
for i in range(0, len(times) - 1):
    print("{0} - {1}".format(times[i], times[i + 1]) + (chr(0x1f989) * peoples[i]).rjust(25, " "))
